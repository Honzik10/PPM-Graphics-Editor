    private void jSlider1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider1StateChanged
        // TODO add your handling code here:
        blockFrame();
        int sliderValue = jSlider1.getValue();
        imagePanel.setOryginalImageForDisplay();
        if (sliderValue < 0) {
            imgTransform.transformImage(-sliderValue, -sliderValue, -sliderValue, ilut.getBrightnessLookupTable());
        } else {
            imgTransform.transformImage(sliderValue, sliderValue, sliderValue, ilut.getDarknessLookupTable());
        }
        
        previousSliderValue = sliderValue;
        unblockFrame();
    }//GEN-LAST:event_jSlider1StateChanged

