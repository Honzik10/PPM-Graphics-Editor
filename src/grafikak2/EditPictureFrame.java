    private void divideRgbValueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_divideRgbValueButtonActionPerformed
        // TODO add your handling code here:
        blockFrame();
        int r = Integer.parseInt(rValueTb.getText());
        int g = Integer.parseInt(gValueTb.getText());
        int b = Integer.parseInt(bValueTb.getText());
        if (r > 0 && g > 0 && b > 0) {
            divideRgbValueButton.setEnabled(false);
            imgTransform.transformImage(r, g, b, ilut.getDivideLookupTable());
            divideRgbValueButton.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Nie dzielimy przez 0!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        unblockFrame();
    }//GEN-LAST:event_divideRgbValueButtonActionPerformed

